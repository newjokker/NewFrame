def vgg19_pruned(loaded_state_dict, norm_size=64):
    def build_cfg(state_dict, arch):
        cfg = []
        out_channel = 512

        for i, k in enumerate(state_dict):
            print(k)
            print(state_dict[k].shape)

            if not k.endswith(".bias"):
                cfg.append(state_dict[k].shape[0])
                if k.startswith("features"):
                    out_channel = state_dict[k].shape[0]
        n_classes = state_dict["classifier.3.bias"].shape[0]

        index = 0
        ori_cfg = cfgs[arch]
        for i, channel in enumerate(ori_cfg):
            if channel == "M":
                continue
            else:
                ori_cfg[i] = cfg[index]
                index += 1

        return ori_cfg, n_classes, out_channel

    cfg, n_classes, out_channel = build_cfg(loaded_state_dict, "E")
    model = VGG(make_layers(cfg, batch_norm=False), features_out_c=out_channel,
                                num_classes=n_classes, norm_size=norm_size, init_weights=False)
    return model
